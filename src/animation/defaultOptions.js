
/**
 * Default options for an animation
 * @name defaultOptions
 * @type {Object}
 * @readonly
 * @private
 */
export default {
  /**
   * If true the animated values will be rounded to the nearest whole integer.
   *
   * @name round
   * @const {Boolean}
   */
  round: true,

  /**
   * If true the animation will loop until it is stopped.
   *
   * @name loop
   * @const {Boolean}
   */
  loop: true,
};
